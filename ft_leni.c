int		ft_leni(long long int n)
{
	int		res;

	res = 0;
	if (n < 0)
	{
		n = -n;
		res++;
	}
	if (n == 0)
		return (++res);
	while (n != 0)
	{
		n = n / 10;
		res++;
	}
	return (res);
}
